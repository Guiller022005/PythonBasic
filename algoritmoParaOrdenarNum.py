#Programa q reciba como entrada dos números, y los muestre ordenados de menor a mayor:

print("\nOrdenar dos numeros")
Numeros = [int(input("Ingresa el primer numero: ")), int(input("Ingresa el segundo numero: "))]
print(Numeros.sort())
print(f"\nLos Numeros ingresados ordenados de menor a mayor son: {Numeros}")

